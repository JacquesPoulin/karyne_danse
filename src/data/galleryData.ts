// Fonction utilitaire pour obtenir des éléments par catégorie
export const getItemsByCategory = (category: MediaCategory | 'all') => {
	if (category === 'all') {
		return galleryItems;
	}
	return galleryItems.filter((item) => item.category === category);
};

// Fonction utilitaire pour recherche
export const searchItems = (items: GalleryItem[], searchTerm: string) => {
	if (!searchTerm.trim()) {
		return items;
	}

	const term = searchTerm.toLowerCase();
	return items.filter(
		(item) =>
			item.alt.toLowerCase().includes(term) ||
			item.tags.some((tag) => tag.toLowerCase().includes(term))
	);
};
